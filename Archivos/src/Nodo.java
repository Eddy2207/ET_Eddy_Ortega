public class Nodo {
    private Object valor = 0;
    private Nodo siguiente = null;
    public Nodo() {
        this.valor = valor;
        this.siguiente = siguiente;
    }

    public Object getValor() {
        return valor;
    }

    public void setValor(Object valor) {
        this.valor = valor;
    }

    public Nodo getSiguiente() {
        return siguiente;
    }

    public void setSiguiente(Nodo siguiente) {
        this.siguiente = siguiente;
    }
}

